genre :: Int -> Maybe Genre
genre x
  | x < 0 = Nothing
  | x > 191 = Nothing
  | otherwise = Just . Genre . toEnum $ x

isExtended :: Genre -> Bool
isExtended (Genre x) = x >= 80

